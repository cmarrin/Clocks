class WordBoard
{
public:
    enum class Words {
        Its, Quarter, Half, Past, To, 
        
        OneMinute, TwoeMinute, ThreeeMinute, FoureMinute, FiveeMinute,
        SixeMinute, SeveneMinute, EighteMinute, NineeMinute, TeneMinute, 
        EleveneMinute, TwelveeMinute, ThirteeneMinute, 
        FourteeneMinute, SixteeneMinute, SeventeeneMinute, 
        EighteeneMinute, NineteeneMinute, TwentyeMinute, 
        
        OneHour, TwoHour, ThreeHour, FourHour, FiveHour, SixHour,
        SevenHour, EightHour, NineHour, TenHour, ElevenHour, TwelveHour,
        
        OClock, InThe, At, Night, Morning, Evening, Afternoon,
        
        ItWillBe, Clear, Windy, Partly, Cloudy, Rainy, Snowy, 
        And, Cold, Cool, Warm, Hot, 
    };
    
    WordBoard()
    {
        words = tigrLoadImage("WordClock-Wawati-800.png");
        screen = tigrWindow(words->w, words->h, "Hello", TIGR_AUTO);
    }
    
    void update() { tigrUpdate(screen); }
    
    bool done() { return tigrClosed(screen); }
    
    void finish() { tigrFree(screen); }
    
    void setLights(const uint8_t* lightState)
    {
        tigrBlit(screen, words, 0, 0, 0, 0, words->w, words->h);
        
        // Assume we start with all lights on
        for (int i = 0; i < 256; ++i) {
            if (lightState[i] == 0) {
                tigrFillRect(screen, (i % 16) * sizeX + originX, (i / 16) * sizeY + originY, sizeX, sizeY, tigrRGBA(0x00, 0x00, 0x00, 0xff));
            }
        }
    }
    
    void setBoxes()
    {
        tigrBlit(screen, words, 0, 0, 0, 0, words->w, words->h);
        
        for (int i = 0; i < 256; ++i) {
            tigrRect(screen, (i % 16) * sizeX + originX, (i / 16) * sizeY + originY, sizeX, sizeY, tigrRGBA(0xff, 0x00, 0x00, 0xff));
        }
    }
    
private:
    Tigr* words = nullptr;
    Tigr* screen = nullptr;
    
    int originX = 10;
    int originY = 11;
    int sizeX = 49;
    int sizeY = 49;
};

