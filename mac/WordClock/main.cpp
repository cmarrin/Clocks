int main(int argc, const char * argv[])
{
    WordBoard board;
    
    int currentTest = 0;
    
    float rate = 5;
    
    float elaspedTime = tigrTime();
    float lastUpdateTime = elaspedTime;
    bool needUpdate = true;

    while (!board.done())
    {
        elaspedTime += tigrTime();
        if (elaspedTime - lastUpdateTime > rate) {
            needUpdate = true;
            lastUpdateTime = elaspedTime;
            if (++currentTest >= 3) {
                currentTest = 0;
            }
        }
            
        if (needUpdate) {
            needUpdate = false;
            const uint8_t* test;
            switch(currentTest) {
                default:
                case 0: test = test1; break;
                case 1: test = test2; break;
                case 2: test = test3; break;
            }
            
            board.setLights(test);
            //board.setBoxes();
        }
        board.update();
    }
    board.finish();

    return 0;
}
